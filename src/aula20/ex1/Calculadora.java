    private void calcular() {
        int resultado = 0;
        
        switch (this.operacao) {
            case '+' :
                resultado = this.n1 + this.n2;
                break;
            case '-':
                resultado = this.n1 - this.n2;
                break;
            case '/':
                resultado = this.n1 / this.n2;
                break;
            case '*':
                resultado = this.n1 * this.n2;
                break;
            default:
                break;
        }
        tfResultado.setText(String.valueOf(resultado));
    }

    private void limpar() {
        tfResultado.setText("");
    }
